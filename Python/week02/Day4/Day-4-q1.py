#Write a program to take in 2 numbers as input and print their +, -, /,//, % and *
num1= int(input("First Number: "))
num2= int(input("Second Number: "))
print("The sum of two numbers=", num1+num2)
print("The substraction of two numbers=", num1-num2)
print("The division of two numbers=", num1/num2)
print("The Floor division of two numbers=", num1//num2)
print("The modulus of two numbers=", num1%num2)
print("The multiplication  of two numbers=", num1*num2)